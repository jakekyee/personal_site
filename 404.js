function randomnum(min, max) {
    min = Math.ceil(min);
    max = Math.floor(max);
    return Math.floor(Math.random() * (max - min) + min); // The maximum is exclusive and the minimum is inclusive
  }

function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

